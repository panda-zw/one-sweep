use anyhow::Result;

use crate::models::safety::SafetyLevel;
use crate::models::scan::{ScanCategory, ScanItem};

use super::{calculate_dir_size_async, get_last_modified, hash_id};

/// Scan for Ruby gem and bundler caches.
pub async fn scan_ruby_cache() -> Result<Vec<ScanItem>> {
    let home = dirs::home_dir().ok_or_else(|| anyhow::anyhow!("No home directory"))?;
    let mut items = Vec::new();

    let cache_locations = [
        (home.join(".gem"), "Ruby gem cache", "Downloaded gem files - re-downloaded with gem install"),
        (home.join(".bundle/cache"), "Bundler cache", "Cached bundle downloads - re-downloaded with bundle install"),
        (home.join("Library/Caches/com.apple.rubygems"), "RubyGems cache", "System RubyGems cache - regenerated automatically"),
    ];

    for (path, name, description) in cache_locations {
        if !path.exists() {
            continue;
        }
        let size = calculate_dir_size_async(&path).await?;
        if size < 10_000_000 {
            continue;
        }
        let path_str = path.to_string_lossy().to_string();
        items.push(ScanItem {
            id: hash_id(&path_str, "ruby_cache"),
            path: path_str,
            display_name: name.to_string(),
            description: description.to_string(),
            size_bytes: size,
            safety: SafetyLevel::Green,
            category: ScanCategory::RubyCache,
            last_modified: get_last_modified(&path),
        });
    }

    Ok(items)
}

