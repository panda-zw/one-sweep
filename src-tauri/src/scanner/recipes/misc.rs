/// Scan for IDE and editor caches (JetBrains, VS Code, Cursor).
pub async fn scan_ide_caches() -> Result<Vec<ScanItem>> {
    let home = dirs::home_dir().ok_or_else(|| anyhow::anyhow!("No home directory"))?;
    let mut items = Vec::new();

    // VS Code / Cursor workspace storage
    let ide_caches = [
        (home.join("Library/Application Support/Code/Cache"), "VS Code cache", "VS Code application cache - regenerated on launch"),
        (home.join("Library/Application Support/Code/CachedData"), "VS Code cached data", "VS Code cached extension data - regenerated on launch"),
        (home.join("Library/Application Support/Code/CachedExtensionVSIXs"), "VS Code extension cache", "Downloaded extension packages - re-downloaded when needed"),
        (home.join("Library/Application Support/Cursor/Cache"), "Cursor cache", "Cursor editor cache - regenerated on launch"),
        (home.join("Library/Application Support/Cursor/CachedData"), "Cursor cached data", "Cursor cached extension data - regenerated on launch"),
    ];

    for (path, name, description) in ide_caches {
        if !path.exists() {
            continue;
        }
        let size = calculate_dir_size_async(&path).await?;
        if size < 50_000_000 {
            continue;
        }
        let path_str = path.to_string_lossy().to_string();
        items.push(ScanItem {
            id: hash_id(&path_str, "ide_caches"),
            path: path_str,
            display_name: name.to_string(),
            description: description.to_string(),
            size_bytes: size,
            safety: SafetyLevel::Green,
            category: ScanCategory::IdeCaches,
            last_modified: get_last_modified(&path),
        });
    }

    // JetBrains old version caches - scan for dirs in ~/Library/Caches/JetBrains
    let jb_cache_dir = home.join("Library/Caches/JetBrains");
    if jb_cache_dir.exists() {
        if let Ok(entries) = std::fs::read_dir(&jb_cache_dir) {
            for entry in entries.flatten() {
                if entry.file_type().map(|ft| ft.is_dir()).unwrap_or(false) {
                    let path = entry.path();
                    let size = calculate_dir_size_async(&path).await?;
                    if size < 50_000_000 {
                        continue;
                    }
                    let name = entry.file_name().to_string_lossy().to_string();
                    let path_str = path.to_string_lossy().to_string();
                    items.push(ScanItem {
                        id: hash_id(&path_str, "ide_caches"),
                        path: path_str,
                        display_name: format!("JetBrains {} cache", name),
                        description: "JetBrains IDE cache - regenerated by the IDE".to_string(),
                        size_bytes: size,
                        safety: SafetyLevel::Green,
                        category: ScanCategory::IdeCaches,
                        last_modified: get_last_modified(&path),
                    });
                }
            }
        }
    }

    Ok(items)
}
